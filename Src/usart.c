uint8_t netUartSend(uint8_t *buffer, uint32_t size)
{
	EventBits_t eventValue;
	if(netTxData == NULL)
	{
		printf("mqtt send buf is null\n");
		return -1;
	}
	
	if (size > MQTT_TX_BUF_SIZE )
	{
		printf("the data too long, the max lenth is %d\n", MQTT_TX_BUF_SIZE);
		return -1;
	}

	eventValue = xEventGroupWaitBits(netEventHandler, EVENTBIT_MQTT_UART_TC, pdTRUE, pdFALSE, (MQTT_UART_WAITTIME * 2));

	if ((eventValue & EVENTBIT_MQTT_UART_TC) != 0)
	{
		memcpy(netTxData, buffer, size);
		HAL_UART_Transmit_DMA(netUart, netTxData, size);
	}
	else
	{
		printf("send to network failed\n");
	}

	return 0;

}




